package Lec3;

public class PossitiveAndNagative {

    public static void main(String[] args) {
        
        int num = -10;

        if(num>0){
            System.out.println("the number is Possitive");
        }else if(num<0){
            System.out.println("The number is Nagative");
        }else {
            System.out.println("the number is Zero");
        }
        
    }
}
