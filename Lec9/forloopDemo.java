public class forloopDemo {
    public static void main(String[] args) {
        // First way
        for(int i = 0; i<=10 ; i++){
            System.out.println(i);
        }

        // sec way
        int i = 0;
        for(; i<=10 ; i++){
            System.out.println(i);
        }
        // third way
        int k = 0;
        for(; k<=10 ;){
            System.out.println(i);
             k++;
        }


        // four way
        for(int j = 0; j<=10 ;){
            System.out.println(i);
             j++;
        }
    }
}
